    private void StepIndex(){
        if(IndexMax > Index){
            Index++;
        }else{
            StepEnd();
            return;
        }
        
        CardLayout cl = (CardLayout) MainPanel.getLayout();
        cl.show(MainPanel, Panels[Index-1]);
    }
    
    private void ShowIndex(int index){     
        CardLayout cl = (CardLayout) MainPanel.getLayout();
        cl.show(MainPanel, Panels[index]);
    }
    
